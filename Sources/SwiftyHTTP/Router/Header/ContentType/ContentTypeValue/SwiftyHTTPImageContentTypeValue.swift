import Foundation

enum SwiftyHTTPImageContentTypeValue: String, SwiftyHTTPContentTypeValue {
    case gif
    case jpeg
    case png
    case tiff
    case vndMicrosoftIcon = "vnd.microsoft.icon"
    case xIcon = "x-icon"
    case vndDjvu = "vnd.djvu"
    case svgXml = "svg+xml"
}
